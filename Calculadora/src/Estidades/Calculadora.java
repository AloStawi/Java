package Estidades;

public class Calculadora {
	
	public Calculadora() 
	{
	}
	
	
	public int Soma(int a, int b) {
	int s = a + b;
	return s;
	}
	
	public int Sub(int a, int b)
	{
		int s = a - b;
		return s;
	}
	
	public int Div(int a, int b)
	{
		int s = a/b;
		return s;
	}
	
	public int Mult(int a, int b)
	{
		int s = a * b;
		return s;
	}

}
